def colormap(rgb=False, maximum=255):
    """
    Args:
        rgb (bool): whether to return RGB colors or BGR colors.
        maximum (int): either 255 or 1
    Returns:
        ndarray: a float32 array of Nx3 colors, in range [0, 255] or [0, 1]
    """
    assert maximum in [255, 1], maximum
    c = _COLORS * maximum
    if not rgb:
        c = c[:, ::-1]
    return c


def get_spaced_colors(n, maximum=255):
    """
    Args:
        n (int): amount of color values
        maximum (int): either 255 or 1
    Returns:
        ndarray: a list of n colors evenly spaced within the _COLORS space
    """
    interval = int(len(_COLORS) / n)

    colors = [tuple((_COLORS[idx] * maximum).astype(np.int)) for idx in range(0,len(_COLORS),interval)]

    return colors
